function addAndSubtract(a, b, c) {
    let sumOfNumbers = a + b;
    let subtractSecondTwoNumbers = sumOfNumbers - c;
    return subtractSecondTwoNumbers;
}

console.log(addAndSubtract(23, 6, 10)); // 19
addAndSubtract(23, 6, 10);  // 19
addAndSubtract(1, 17, 30);  // -12
addAndSubtract(42, 58, 100);    // 0